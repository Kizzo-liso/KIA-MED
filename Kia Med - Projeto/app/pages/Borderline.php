    <section class="container-texto">

        <p class="apresentacao-tema">Quais as causas do Transtorno Borderline?</p>

        <p>Assim como outros transtornos mentais, os fatores genéticos podem ser preponderantes para o desenvolvimento do Transtorno Borderline, ao mesmo tempo que as marcas de situações traumáticas vivenciadas precocemente  ou abusos físicos e emocionais e ainda  vivenciar  ambiente de grande instabilidade emocional, podem contribuir. Ou seja, fatores congênitos e de criação podem contribuir, mas não são sozinhos determinantes.</p>

    </section>

    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Como é o diagnóstico do Transtorno Borderline?</p>

        <p>O paciente passa por uma avaliação junto ao psiquiatra, que a faz de forma criteriosa, ao perceber padrões de comportamento e o conjunto de sintomas característicos deste transtorno.

            Como o Borderline é um transtorno de personalidade, seu diagnóstico deve ser ainda mais minucioso, através da observação do psiquiatra e dos relatos do paciente, para que haja a diferenciação correta de outros, como o transtorno bipolar, que pode ser facilmente confundido por leigos.</p>

    </section>

    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">O tratamento do Transtorno Borderline</p>

        <p>O Transtorno Borderline deve ser tratado pelo profissional da saúde mental, em geral a melhor opção é  um trabalho conjunto do psiquiatra e do psicólogo. Ambos são capazes de realizar o diagnóstico do transtorno e fornecer a orientação adequada ao tratamento.
            Os medicamentos utilizados para tratar o Transtorno de Borderline servem para amenizar os sintomas de forma isolada, podendo ser uma combinação de antidepressivos, estabilizadores de humor e antipsicóticos. Eles podem contribuir para fins específicos, como ajudar em uma possível depressão, auxiliar no controle da raiva e atuar na diminuição da impulsividade.
            Junto a isso, a psicoterapia é indicada, pois ela ajuda o paciente a ter noção do seu comportamento e a buscar métodos de encará-lo de forma a domá-lo e mitigar os prejuízos pessoais e sociais que tal comportamento possa causar.</p>

    </section>

    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Como lidar com uma pessoa com borderline?</p>

        <p>Conviver com uma pessoa com Transtorno de Borderline requer uma dose extra de paciência, afeto e sabedoria. O borderline vive em medo constante do abandono e da rejeição e qualquer sinal pode ser motivo para uma tristeza profunda ou episódios de impulsividade e fúria. Eles podem ser amantes maravilhosos, amigos super divertidos e pessoas com as quais você pode contar, mas também podem apresentar suas sombras dependendo as situações. </p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Borderline: uma personalidade que precisa de atenção</p>

        <p>Os borderlines podem viver uma vida com uma montanha-russa de sentimentos, mas quando tratados e sob controle, são pessoas maravilhosas. Por se tratar de transtorno de personalidade, a atenção deve ser constante e a busca por ajuda deve ser sempre considerada.

 

            Também é importante salientar que se você gosta ou ama alguém com este transtorno, tutelá-lo e colocar-se como a solução de seus problemas não é saudável para você. A sua contribuição é importante, mas a pessoa também precisa buscar e aceitar ajuda.</p>


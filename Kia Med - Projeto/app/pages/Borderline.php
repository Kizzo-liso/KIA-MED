            <p class="significado-subtitulo">Transtorno de Personalidade Borderline é o nome do transtorno de saúde mental dado a pessoas que possuam um padrão de comportamento permeado pela impulsividade, instabilidade emocional, medo da rejeição e tendências autodepreciativas.</p>


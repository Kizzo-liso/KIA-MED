    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Sair com amigos</p>

        <p>Um dos sintomas da depressão é a vontade de se isolar. Pessoas depressivas acreditam que ninguém as compreende, além de sofrerem de falta de energia. Por essa razão, evitam momentos sociais. Mas, na verdade, você deve tentar manter a sua vida social movimentada. Não é preciso marcar programas todos os finais de semana. Saídas ocasionais com amigos são o suficiente para aliviar o mau humor e desânimo.</p>

    </section>

    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Meditar</p>

        <p>A meditação é uma prática que reduz a ansiedade e combate pensamentos negativos. Como a depressão desencadeia muitos sentimentos
            e devaneios desagradáveis, meditar ajuda a limpar a mente do indesejável. Praticar a meditação por somente cinco minutos
            diariamente já é capaz de fazer uma grande diferença no seu dia! </p>

    </section>

    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Exercício físico</p>

        <p>As atividades físicas são ótimos remédios naturais para a depressão, ansiedade, transtorno do pânico e outros transtornos mentais.
            Ao movimentar o seu corpo por, pelo menos, 20 minutos ao dia, você estimula a produção e liberação dos “hormônios da felicidade”.</p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Fazer voluntariado</p>

        <p>A depressão potencializa problemas pequenos e faz com que você feche os olhos para o restante do mundo. Conhecer a realidade de outras pessoas e doar o seu tempo e habilidades sem pedir nada em troca lhe ajudará a perceber que o mundo é mais diverso e interessante do que você pensava.</p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Procurar e ter um hobby</p>

        <p>Ter um hobby é importante em todas as etapas da vida. Se você está se perguntando como tratar a depressão na terceira idade, adolescência ou infância, tenha em mente que incentivar a execução de uma atividade prazerosa é indispensável para o bem-estar.</p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Exercitar a gratidão</p>

        <p>Ao agradecer o que você tem, você direciona a sua mente para os elementos positivos presentes em sua vida, como família, amigos, sonhos, trabalho, vitórias, alimentação, moradia, entre outros.</p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Encontrar grupos de apoio</p>

        <p>Grupos de apoio ajudam as pessoas a se sentirem acolhidas. Ao encontrar outros indivíduos na mesma situação, elas compreendem que não estão sozinhas. Muitos já passaram e ainda passam pela mesma situação. Assim, sentem-se menos inadequadas e ainda encontram outras fontes de apoio.</p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Fazer cursos</p>

        <p>A depressão rouba o colorido da vida. Consequentemente, você perde o interesse por atividades que antes considerava agradáveis. Apesar de ser difícil encontrar estímulos para reacender o entusiasmo, a busca por estímulos positivos é necessária. Um curso, por exemplo, pode dar a você motivação e propósito para encarar os dias. Pode ser algo rápido e simples, como um curso de fim de semana, ou um duradouro, como aprender a tocar um instrumento. </p>

    </section>
    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Aproximar-se da natureza</p>

        <p>Outra resposta à pergunta “como tratar a depressão?” é criar oportunidades para interagir com a natureza. Você pode fazer caminhadas em meio à uma paisagem bonita, fazer jardinagem, viajar para a praia ou simplesmente observar a natureza ao seu redor. O contato com as plantas e os animais desperta sentimentos bons, como tranquilidade e contentamento. Além disso, muda o foco das obrigações diárias e estresse. 
        </p>

    </section>

    <section class="conteiner-respirar-melhor">

        <p class="respirar-melhor">Continuar o tratamento</p>

        <p>Talvez você tenha de lutar contra a doença pelo resto da vida, mas essa noção não deve desanimá-lo. Não tenha vergonha de procurar ajuda nem tema julgamentos. Aceite sua situação, pois fugir dela somente agravará o seu sofrimento. Toda vez que você decide cuidar da sua saúde está demonstrando a si mesmo que você se ama.
        </p>

